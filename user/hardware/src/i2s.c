/* I2S1 init function */
void MX_I2S1_Init(uint32_t freq)
{

  hi2s1.Instance = SPI1;
  hi2s1.Init.Mode = I2S_MODE_MASTER_TX;
  hi2s1.Init.Standard =I2S_STANDARD_PHILIPS;//I2S_STANDARD_MSB;// I2S_STANDARD_PCM_LONG;//I2S_STANDARD_PCM_SHORT;//
  hi2s1.Init.DataFormat = I2S_DATAFORMAT_16B;
  hi2s1.Init.MCLKOutput = I2S_MCLKOUTPUT_DISABLE;
  hi2s1.Init.AudioFreq =freq;//I2S_AUDIOFREQ_8K ;//I2S_AUDIOFREQ_8K;//
  hi2s1.Init.CPOL = I2S_CPOL_LOW;
  if (HAL_I2S_Init(&hi2s1) != HAL_OK)
  {
    Error_Handler();
  }

}

